    private void inputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inputActionPerformed
        try {
            if (tp.hasRecord(lantai.getText() + "_" + blok.getText() + "_" + lokasi.getText())) {
                JOptionPane.showMessageDialog(null, "no kartu sudah ada");
            } else {
                boolean status = false;
                if (terisi.isSelected()) {
                    status = true;
                } else if (kosong.isSelected()) {
                    status = false;
                }
                tp.createRecord(blok.getText(), lantai.getText(), lokasi.getText(), status);
                tampilData("");
                JOptionPane.showMessageDialog(null, "Simpan Berhasil");
                lantai.setText("");
                blok.setText("");
                lokasi.setText("");
                buttonGroup1.clearSelection();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_inputActionPerformed

