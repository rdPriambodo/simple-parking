    public void tampilData(String where) {
        try {
            tabModel.getDataVector().removeAllElements();
            tabModel.fireTableDataChanged();
            rs = tp.getAll();

            while (rs.next()) {
                boolean status = rs.getBoolean("status");
                String kosong = "";
                if (status) {
                    kosong = "Terisi";
                } else {
                    kosong = "Kosong";
                }
                Object[] data = {
                    rs.getString("id_parkir"),
                    rs.getString("lantai"),
                    rs.getString("blok"),
                    rs.getString("no_lokasi"),
                    kosong};

                tabModel.addRow(data);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

